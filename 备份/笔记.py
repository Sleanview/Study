#变量是用来存储数据的
a=10 #这是一个变量
print(type(a))
a=(20)
a=(100)
a=('吴老师') #字符串需要加''引号
print(type(a))
#print(a) 使用变量 可以变化的量
# 高级类型

b=() #元组类型
print(type(b))
c=[] #列表类型
print(type(c))
d={} #字典类型
print(type(d))
# 变量的命名规则
# 规则：变量名=数据
# 变量的命名不能以数字开头
# 变量区分大小写
# 关键字不能作为变量名
Name='刘德华'
_age=50
